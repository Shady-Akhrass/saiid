-- SQL Script: إصلاح المشاريع في حالة "مسند لباحث" بدون باحث مسند
-- تاريخ الإنشاء: 2025-12-22
-- الوصف: إصلاح التناقض في البيانات - مشاريع في حالة "مسند لباحث" لكن assigned_researcher_id هو NULL
-- =====================================================
-- 
-- ⚠️ مهم: قم بعمل backup لقاعدة البيانات قبل التنفيذ!
-- =====================================================

-- =====================================================
-- 1. عرض المشاريع التي تحتاج إصلاح
-- =====================================================

-- عرض المشاريع في حالة "مسند لباحث" لكن بدون باحث مسند
SELECT 
    id,
    serial_number,
    project_name,
    status,
    assigned_researcher_id,
    assigned_photographer_id,
    assigned_by,
    assignment_date,
    created_at
FROM `project_proposals`
WHERE `status` = 'مسند لباحث'
  AND (`assigned_researcher_id` IS NULL OR `assigned_researcher_id` = 0);

-- =====================================================
-- 2. إصلاح المشاريع: إرجاعها إلى حالة "تم التوريد"
-- =====================================================

-- إرجاع المشاريع في حالة "مسند لباحث" بدون باحث مسند إلى حالة "تم التوريد"
UPDATE `project_proposals`
SET 
    `status` = 'تم التوريد',
    `assigned_researcher_id` = NULL,
    `assigned_by` = NULL,
    `assignment_date` = NULL,
    `updated_at` = NOW()
WHERE `status` = 'مسند لباحث'
  AND (`assigned_researcher_id` IS NULL OR `assigned_researcher_id` = 0);

-- =====================================================
-- 3. التحقق من النتيجة
-- =====================================================

-- عرض عدد المشاريع التي تم إصلاحها
SELECT 
    COUNT(*) as fixed_projects_count
FROM `project_proposals`
WHERE `status` = 'تم التوريد'
  AND `assigned_researcher_id` IS NULL
  AND `updated_at` >= DATE_SUB(NOW(), INTERVAL 1 MINUTE);

