-- الخطوة 1: إضافة "يجب إعادة المونتاج" إلى ENUM (مع الاحتفاظ بـ "معاد مونتاجه" مؤقتاً)
ALTER TABLE `project_proposals` 
MODIFY COLUMN `status` ENUM(
    'جديد',
    'قيد التوريد',
    'تم التوريد',
    'قيد التوزيع',
    'جاهز للتنفيذ',
    'تم اختيار المخيم',
    'قيد التنفيذ',
    'منفذ',
    'في المونتاج',
    'تم المونتاج',
    'وصل للمتبرع',
    'ملغى',
    'معاد مونتاجه',
    'يجب إعادة المونتاج',
    'مؤجل'
) NOT NULL DEFAULT 'جديد';

-- الخطوة 2: تحديث جميع المشاريع التي حالتها "معاد مونتاجه" إلى "يجب إعادة المونتاج"
UPDATE `project_proposals` 
SET `status` = 'يجب إعادة المونتاج', 
    `updated_at` = NOW()
WHERE `status` = 'معاد مونتاجه';

-- الخطوة 3: إزالة "معاد مونتاجه" من ENUM (بعد تحديث جميع البيانات)
ALTER TABLE `project_proposals` 
