-- حذف Index على rejection_reason
ALTER TABLE `project_proposals` 
DROP INDEX `idx_rejection_reason`;

-- ============================================
-- 2. حذف الأعمدة
-- ============================================

-- حذف جميع أعمدة سبب الرفض
ALTER TABLE `project_proposals` 
DROP COLUMN `rejection_reason`,
DROP COLUMN `rejection_message`,
DROP COLUMN `admin_rejection_reason`,
DROP COLUMN `media_rejection_reason`;

-- ============================================
-- 3. التحقق من الحذف
-- ============================================

-- التحقق من عدم وجود الأعمدة
SELECT 
    COLUMN_NAME as 'اسم العمود'
FROM information_schema.COLUMNS 
WHERE TABLE_SCHEMA = DATABASE()
    AND TABLE_NAME = 'project_proposals'
    AND COLUMN_NAME LIKE '%rejection%';

